export function getCalculatorBySlug(slug: string): Calculator | undefined {
  return calculators.find((calc) => calc.slug === slug);
}

export function getCalculatorsByCategory(category: CalculatorCategory): Calculator[] {
  return calculators.filter((calc) => calc.category === category);
}

export function getPopularCalculators(limit: number = 8): Calculator[] {
  return calculators.filter((calc) => calc.popular).slice(0, limit);
}

export function searchCalculators(query: string): Calculator[] {
  const lowerQuery = query.toLowerCase();
  return calculators.filter(
    (calc) =>
      calc.name.toLowerCase().includes(lowerQuery) ||
      calc.description.toLowerCase().includes(lowerQuery) ||
      calc.category.toLowerCase().includes(lowerQuery)
  );
}
