export function getResourceBySlug(slug: string): Resource | undefined {
  return resources.find((resource) => resource.slug === slug);
}

export function getFeaturedResources(limit: number = 4): Resource[] {
  return resources.filter((resource) => resource.featured).slice(0, limit);
}

export function getTrendingResources(limit: number = 4): Resource[] {
  return resources.filter((resource) => resource.trending).slice(0, limit);
}

export function getResourcesByCategory(category: string): Resource[] {
  return resources.filter((resource) => resource.category.toLowerCase() === category.toLowerCase());
}

